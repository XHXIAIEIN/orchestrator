# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

def _blit(r: Pico8Renderer, sprite: list[str], x: int, y: int,
          ghost: bool = False, tint: int | None = None):
    for row_idx, row in enumerate(sprite):
        for col_idx, ch in enumerate(row):
            col = _HEX.get(ch, -1)
            if col < 0:
                continue
            px, py = x + col_idx, y + row_idx
            if 0 <= px < r.width and 0 <= py < r.height:
                if ghost:
                    r.pset(px, py, GHOST)
                elif tint is not None:
                    r.pset(px, py, tint)
                else:
                    r.pset(px, py, col)



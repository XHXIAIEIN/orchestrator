class GWScene:
    """Game & Watch scene — clean, minimal, entity-focused."""

    DEPT_ORDER = ["engineering", "quality", "protocol",
                  "operations", "security", "personnel"]

    def __init__(self):
        self.frame = 0

    def update(self, snap: SystemSnapshot):
        self.frame += 1

    def draw(self, r: Pico8Renderer, snap: SystemSnapshot):
        W, H = r.width, r.height
        cx, cy = W // 2, H // 2
        r.cls(0)

        # ── Entity — centered, the only star ──
        ex, ey = cx - 12, cy - 12
        anim, speed = self._pick_anim(snap)
        frame_idx = (self.frame // speed) % len(anim)

        # Ghost: idle silhouette always faintly visible
        _blit(r, ANIM_IDLE[0], ex, ey, ghost=True)

        # Active frame
        _blit(r, anim[frame_idx], ex, ey)

        # ── Bottom status — single line of dots ──
        sy = H - 6
        # Six dept dots: lit if active, ghost if not
        dot_start = cx - 17
        for i, dept in enumerate(self.DEPT_ORDER):
            dx = dot_start + i * 6
            col_idx = DEPT_COLORS.get(dept, 12)
            if dept in snap.dept_active:
                r.rectfill(dx, sy, dx + 2, sy + 2, col_idx)
            else:
                r.rectfill(dx, sy, dx + 2, sy + 2, GHOST)

        # Container status dot — far right
        if snap.container_up:
            r.rectfill(dot_start + 40, sy, dot_start + 42, sy + 2, GREEN)
        else:
            blink = (self.frame // 5) % 2
            r.rectfill(dot_start + 40, sy, dot_start + 42, sy + 2,
                       ALERT_COL if blink else GHOST)

    def _pick_anim(self, snap: SystemSnapshot) -> tuple[list, int]:
        """Return (animation_frames, ticks_per_frame) for current state."""
        if not snap.container_up:
            return ANIM_DOWN, 8
        if snap.has_alert:
            return ANIM_ALERT, 3  # fast flash
        if snap.idle_seconds > 120:
            return ANIM_SLEEP, 10  # slow breathing
        if snap.active_task_count > 0:
            return ANIM_WORK, 4   # medium spin
        return ANIM_IDLE, 6       # gentle breathing

    def _calc_health(self, snap: SystemSnapshot) -> int:
        if not snap.container_up:
            return 0
        if snap.has_alert:
            return 1
        if snap.active_task_count > 3:
            return 3
        if snap.idle_seconds > 300:
            return 4
        return 5

    def _calc_energy(self, snap: SystemSnapshot) -> int:
        if not snap.container_up:
            return 0
        if snap.active_task_count >= 3:
            return 4
        if snap.active_task_count > 0:
            return 2
        if snap.collecting:
            return 3
        return 1
